package com.dozken.constants;

public class Parameter {

    private Double a;
    private Double b;
    private Double c;

    Parameter(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public Double getA() {
        return a;
    }

    public Double getB() {
        return b;
    }

    public Double getC() {
        return c;
    }
}
